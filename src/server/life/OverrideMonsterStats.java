package server.life;

public class OverrideMonsterStats {
    private int exp, hp, mp;

    public int getExp() {
        return exp;
    }

    public void setOExp(int exp) {
        this.exp = exp;
    }

    public int getHp() {
        return hp;
    }

    public void setOHp(int hp) {
        this.hp = hp;
    }

    public int getMp() {
        return mp;
    }

    public void setOMp(int mp) {
        this.mp = mp;
    }
}
